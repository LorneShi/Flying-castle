function flyCHN(str)
     if not str then return end
	--特殊格式转化
	if str == "Frozen!" then return "冻结！"
	elseif str == "Resist!" then return "抵抗！"
	elseif str == "RESURRECT!" then return "复活！"
	elseif str == "LOW HEALTH!" then return "生命过低！"
	elseif str == "LEVEL UP!" then return "升级！"
	elseif str == "RANK UP!!" then return "级别升高！！"
	elseif str == "Falls dead!" then return "陷入死亡！"
	elseif str == "OVERKILL" then return "过度杀伤"
	elseif str == "+Off-balance" then return "失去平衡 获得"
	elseif str == "-Off-balance" then return "失去平衡 消失"
	elseif str == "+Low. fire resist" then return "火焰抗性降低 获得"
	elseif str == "+Low. cold resist" then return "寒冰抗性降低 获得"
	elseif str == "+Low. nature resist" then return "自然抗性降低 获得"
	elseif str == "+Low. physical resist" then return "物理抗性降低 获得"
	elseif str == "-Low. fire resist" then return "火焰抗性降低 消失"
	elseif str == "-Low. cold resist" then return "寒冰抗性降低 消失"
	elseif str == "-Low. nature resist" then return "自然抗性降低 消失"
	elseif str == "-Low. physical resist" then return "物理抗性降低 消失"
	elseif str == "+Off-guard" then return "猝不及防 获得"
	elseif str == "-Off-guard" then return "猝不及防 消失"
	elseif str == "CRITICAL VELOCITY!" then return "零界速度！"
	elseif str == "+ENCUMBERED!" then return "超重！"
	elseif str == "-ENCUMBERED!" then return "超重解除！"

      end
      if string.find(str,"available") then return string.gsub(str,"available","可使用") end
      if string.find(str,"KILL") then return string.gsub(str,"KILL","杀死 ") end
      if string.find(str," kills!") then return string.gsub(str," kills!"," 击杀！") end
      if string.find(str,"Round Clear! ") then return string.gsub(str,"Round Clear! ","回合结束！ ") end
      --buff转化
      str=string.gsub(str," ","")
      str=string.gsub(str,"'","")

  for i ,v in pairs (flytextCHN) do
     if i==string.gsub(str,"+","") then return flytextCHN[i].." 获得" end
     if i==string.gsub(str,"-","") then return flytextCHN[i].." 消失" end
  end
  return str
end
