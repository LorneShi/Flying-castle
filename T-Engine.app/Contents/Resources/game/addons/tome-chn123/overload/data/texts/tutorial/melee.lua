return [[近战攻击类似于移动，向你的目标方向移动或者左键点击就可以攻击它。
如果目标不是敌对的你会和他交谈或者和他交换位置。

近战攻击主要用到一下5个属性。
* #GOLD#攻击#WHITE#：你的命中几率。
* #GOLD#伤害#WHITE#：你所造成的伤害值。
* #GOLD#闪避#WHITE#：你躲闪攻击的几率。
* #GOLD#护甲值#WHITE#：能减少的伤害量。
* #GOLD#护甲穿透#WHITE#：你可以忽视目标多少护甲值。

你可以在角色面板中查看你的这些属性值。
按'C'键或右键点击你的目标选择角色面板打开。

你发现了一些敌对的果冻怪围着一件装备。
攻击他们！
]]
